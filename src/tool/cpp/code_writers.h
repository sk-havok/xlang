    inline void write_class_override_defaults(writer& w, std::map<std::string, interface_info> const& interfaces)
    {
        bool first{ true };

        for (auto&&[name, info] : interfaces)
        {
            if (!info.overridable)
            {
                continue;
            }

            if (first)
            {
                first = false;
                w.write(",\n    %T<D>", name);
            }
            else
            {
                w.write(", %T<D>", name);
            }
        }
    }

