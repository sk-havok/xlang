    inline void write_fast_component_forwarders(writer& w, TypeDef const& type, std::vector<factory_type> const&)
    {
        auto type_name = type.TypeName();
        auto type_namespace = type.TypeNamespace();
        bool default_constructor{};

        // TODO: Clean this up
        w.param_names = true;

        for (auto&& info : get_fast_interfaces(w, type))
        {
            if (info.name.empty())
            {
                default_constructor = true;

                auto format = R"(        @::% ActivateInstance() const
        {
            return {};
        }
)";

                w.write(format, type_namespace, type_name);
            }
            else if (info.instance)
            {
                auto format = R"(        % %(@::% const& winrt_impl_this%) const
        {
            return get_self<implementation::%>(winrt_impl_this)->%(%);
        }
)";

                for (auto&& method : info.methods)
                {
                    method_signature signature{ method };
                    auto method_name = get_name(method);

                    w.write(format,
                        signature.return_signature(),
                        method_name,
                        type_namespace,
                        type_name,
                        bind<write_implementation_params>(signature),
                        type_name,
                        method_name,
                        bind<write_consume_args>(signature));
                }
            }
            else if (info.activatable)
            {
                auto format = R"(        @::% %(%) const
        {
            return { % };
        }
)";

                for (auto&& method : info.methods)
                {
                    method_signature signature{ method };
                    auto method_name = get_name(method);

                    w.write(format,
                        type_namespace,
                        type_name,
                        method_name,
                        bind<write_implementation_params>(signature),
                        bind<write_consume_args>(signature));
                }
            }
            else if (info.statics)
            {
                auto format = R"(        % %(%) const
        {
            return implementation::%::%(%);
        }
)";

                for (auto&& method : info.methods)
                {
                    method_signature signature{ method };
                    auto method_name = get_name(method);

                    w.write(format,
                        signature.return_signature(),
                        method_name,
                        bind<write_implementation_params>(signature),
                        type_name,
                        method_name,
                        bind<write_consume_args>(signature));
                }
            }
        }

        if (!default_constructor)
        {
            auto format = R"(        [[noreturn]] @::% ActivateInstance() const
        {
            throw hresult_not_implemented();
        }
)";

            w.write(format, type_namespace, type_name);
        }
    }

